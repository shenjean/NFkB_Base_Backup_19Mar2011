<p class="header">About NF-kB Base</p>
The NF-kB Base contains 413 manually curated records of 
protein sequences within the eukaryotic NF-kB family<br>

<p><u>The current release (Beta 2.1) contains:</u><br>
22 C-rel records<br>
41 Dorsal records<br>
29 Dif records<br>
70 Nfkb1 records<br>
59 Nfkb2 records<br>
95 RelA records<br>
19 RelB records<br>
71 Relish records<br>
